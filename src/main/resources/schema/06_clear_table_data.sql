-- 说明：用于清除所有表中的数据，但保留表结构
-- 与03_truncate_tables.sql不同，此脚本使用DELETE语句，不重置自增ID
-- 警告：此脚本会永久删除所有数据，请谨慎使用！
-- ===============================================================

-- 关闭外键约束检查
SET FOREIGN_KEY_CHECKS = 0;

-- 清除文章HTML内容表的数据
DELETE FROM article_full_html;

-- 清除文章基本信息表的数据
DELETE FROM article_table;

-- 重新启用外键约束
SET FOREIGN_KEY_CHECKS = 1;

-- 提示：执行此脚本后，所有表的数据将被删除，但表结构保持不变
-- 自增ID不会重置，继续从最后一个ID增长
-- 如果需要重置自增ID，请使用03_truncate_tables.sql 